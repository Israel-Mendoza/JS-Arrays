// Pushing, pulling, shifting, and unshifting

let topSongs = [
    "I Saw Her Standing There",
    "Norwegian Woods",
    "Julia",
    "Because"
];

console.log(topSongs);

// Adds an item at the end of the array
topSongs.push("Let It Be");
console.log(topSongs);

// Removes an item from the end of the array
topSongs.pop();
console.log(topSongs);

// Removes an item at the beginning of the array
topSongs.shift();
console.log(topSongs);

// Adds an item at the beginning of the array
topSongs.unshift("Twist and Shout");
console.log(topSongs);


